public class WatchList {

    private String uid;

    private String watchman;

    private String watchdata;

    private String updatetime;

    private String ondutyTitle;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getWatchman() {
        return watchman;
    }

    public void setWatchman(String watchman) {
        this.watchman = watchman;
    }

    public String getWatchdata() {
        return watchdata;
    }

    public void setWatchdata(String watchdata) {
        this.watchdata = watchdata;
    }

    public String getUpdatetime() {
        return updatetime;
    }

    public void setUpdatetime(String updatetime) {
        this.updatetime = updatetime;
    }

    public String getOndutyTitle() {
        return ondutyTitle;
    }

    public void setOndutyTitle(String ondutyTitle) {
        this.ondutyTitle = ondutyTitle;
    }
}
